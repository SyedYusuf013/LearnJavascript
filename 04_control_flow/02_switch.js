const month = 3

switch (month) {
    case 1:
        console.log("jan");
        break;
    case 2:
        console.log("feb");
        break;
    case 3:
        console.log("march");
        break; // info: we use break for get out from the loop and print the value, and if we do not put break then if the correct value comes out the after the it will print all the rest values to.
    case 4:
        console.log("april");
        break;

    default:
        console.log("default print out");
        break;
}







